public class StringConcat {
    public static String greet(String name) {
        return "Hello, " + name + "!";
    }
    
    public static String numberToString(int n) {
        return "The number is: " + n;
    }
    
    public static String multiConcat(String a, int b, String c) {
        return a + " - " + b + " - " + c;
    }
}

